<h2>Use Case:</h2>
<p><strong>Title:</strong> Kyle wants his Steam Profile to reflect his Legend</p>
<p><strong>Description:</strong> Kyle wants his Steam Profile to be Monster Themed.</p>
<p><strong>Name of user &amp; their role:</strong> Kyle; Monster lover, hater of Walls, Passionate about MLG Gaming on Steam.</p>
<p><strong>Usage preconditions:</strong> Must have Steam account and logged in.</p>
<p><strong>Usage postconditions:</strong> Kyle is able to customize his Steam Profile and Interact With Like Minded Gamers/Legends.</p>
<p><strong>Interaction flow:</strong></p>
<ul>
	<li>Kyle enters steam.com into his web browser.</li>
	<li>Server returns Steam Home Store Page.</li>
	<li>Kyle  clicks on "Profile" link on Steam's Nav Bar. "Edit Profile" Option Available.</li>
	<li>Server completes request to edit Kyle's Steam Profile.</li>
	<li>Kyle sees the options to create an about me page, change his profile avatar, create preferences, set backgrounds, add hyperlinks etc,.</li>
	<li>Kyle sets his profile to his desired configuration and saves changes.</li>
	<li>Kyle reloads the page and sees all his settings saved. Next he adds his groups/community pages of like minded Monster and MLG enthusiasts.</li>
	<li>From his profile page, he can see who is on and seamlessly chat/interact, and join his friends in games.</li>
	<li>Kyle is able to share his passion for Gaming, Monster, and Doritos with the Steam Community and the World.</li>
</ul>
<p><strong>Frequency of Use:</strong> All Day, EveryDay</p>
